<h1>Serviço de Informações ao Cidadão - SIC</h1>
<p>O Serviço de Informações ao Cidadão (SIC) é a unidade física existente em todos os órgãos e entidades do poder público, em local identificado e de fácil acesso, para atender o cidadão que deseja solicitar o acesso à informação pública. Os SICs têm como objetivos:</p>
<ul>
	<li>Atender e orientar o público quanto ao acesso a informações</li>
	<li>Conceder o acesso imediato à informação disponível</li>
	<li>Informar sobre a tramitação de documentos nas suas respectivas unidades</li>
	<li>Protocolizar documentos e requerimentos de acesso a informações</li>
</ul>